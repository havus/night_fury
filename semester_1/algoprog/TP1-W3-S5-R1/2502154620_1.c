#include <stdio.h>

int main() {
  // + addition or unary plus
  // - subtraction or unary minus
  // * multiplication
  // / division
  // % modulo division

  int num1 = 3, num2 = 7;

  printf("Operator +, contohnya adalah: %d + %d = %d \n", num1, num2, num1 + num2);
  printf("Operator -, contohnya adalah: %d - %d = %d \n", num1, num2, num1 - num2);
  printf("Operator *, contohnya adalah: %d * %d = %d \n", num1, num2, num1 * num2);
  printf("Operator /, contohnya adalah: %d / %d = %d \n", num1, num2, num1 / num2);
  printf("Operator %%, contohnya adalah: %d %% %d = %d \n", num2, num1, num2 % num1);

  return 0;
}
